package javapractice;

public class unaryOperator {
    public static void main(String[]args){
        int value1 =20;
        int value2 =40;
        if (value1==value2)
            System.out.println("value1 == value2");
        if (value1!=value2)
            System.out.println("value1 != value2");
        if (value1>value2)
            System.out.println("value1 > value2");
        if (value1>=value2)
            System.out.println("value1 >= value2");
        if (value1<value2)
            System.out.println("value1 < value2");
        if (value1<=value2)
            System.out.println("value1 <= value2");


    }
}
