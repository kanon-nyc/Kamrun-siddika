package javapractice;

public class conditionalOperators {
    public static void main(String[]args){
        int value1 = 2;
        int value2 = 4;
        if ((value1==2) && (value2==4))
            System.out.println("value1 is 2 and value2 is 4");
        if ((value1 == 2) || (value2==4))
            System.out.println("value1 is 2 or value2 is 4");

    }
}
